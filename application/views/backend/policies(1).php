    document.addEventListener('DOMContentLoaded', function() {
        const content = document.getElementById('content');
        const pagination = document.getElementById('pagination');
        const nextButton = document.getElementById('nextButton');
        const prevButton = document.getElementById('prevButton');
        const itemsPerPage = 30; // Number of items to display per page
        let currentPage = 1;
        const totalItems = content.children.length;

        // Function to display items for the given page number
        function displayPage(page) {
            const start = (page - 1) * itemsPerPage;
            const end = start + itemsPerPage;
            for (let i = 0; i < totalItems; i++) {
                if (i >= start && i < end) {
                    content.children[i].style.display = '';
                } else {
                    content.children[i].style.display = 'none';
                }
            }
            updatePaginationLinks(page);
        }

        // Function to generate pagination links
        function generatePagination() {
            const totalPages = Math.ceil(totalItems / itemsPerPage);
            const paginationInfo = `Page ${currentPage} of ${totalPages}`;
            let links = '';
            for (let i = 1; i <= totalPages; i++) {
                links += `<a href="#${i}">${i}</a> `;
            }
            pagination.innerHTML = paginationInfo + '<br>' + links;
            updatePaginationLinks(currentPage);
        }

        // Function to update pagination links
        function updatePaginationLinks(currentPage) {
            const paginationLinks = document.querySelectorAll('#pagination a');
            paginationLinks.forEach(link => {
                const pageNum = parseInt(link.textContent);
                if (pageNum === currentPage) {
                    link.classList.add('active-page');
                } else {
                    link.classList.remove('active-page');
                }
            });
        }

        // Display the first page by default
        displayPage(currentPage);

        // Generate pagination links
        generatePagination();

        // Event listener for clicking on a page number link
        pagination.addEventListener('click', function(event) {
            if (event.target.tagName === 'A') {
                const pageNum = parseInt(event.target.textContent);
                displayPage(pageNum);
                currentPage = pageNum;
                generatePagination(); // Update pagination info
            }
        });

        // Event listener for the "Next" button
        nextButton.addEventListener('click', function() {
            currentPage++;
            if (currentPage > Math.ceil(totalItems / itemsPerPage)) {
                currentPage = Math.ceil(totalItems / itemsPerPage);
            }
            displayPage(currentPage);
            generatePagination(); // Update pagination info
        });

        // Event listener for the "Previous" button
        prevButton.addEventListener('click', function() {
            currentPage--;
            if (currentPage < 1) {
                currentPage = 1;
            }
            displayPage(currentPage);
            generatePagination(); // Update pagination info
        });
    });
</script>









